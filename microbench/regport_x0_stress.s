# Register Port X0 Stress Test
# 测试策略：使用x0作为目标寄存器，避免更新regCache
# 目的：让所有源操作数都需要争夺读端口，更容易触发端口仲裁失败
# v2 KunminghuScheduler有12个整数读端口，我们需要超过这个限制

.section .text
.global _start

_start:
    # 高强度x0写入测试 - 所有指令写入x0，不更新regCache
    # 这样所有源操作数都必须从寄存器文件读取，造成最大读端口压力
    # 14条指令 x 2个源操作数 = 28个读端口需求，远超12个端口限制

    # 第一组：连续高密度读端口需求
    add x0, x1, x2     # 写入x0，源操作数需要读端口
    add x0, x3, x4     # 写入x0，源操作数需要读端口
    add x0, x5, x6     # 写入x0，源操作数需要读端口
    add x0, x7, x8     # 写入x0，源操作数需要读端口
    add x0, x9, x10    # 写入x0，源操作数需要读端口
    add x0, x11, x12   # 写入x0，源操作数需要读端口
    add x0, x13, x14   # 写入x0，源操作数需要读端口
    add x0, x15, x16   # 写入x0，源操作数需要读端口
    add x0, x17, x18   # 写入x0，源操作数需要读端口
    add x0, x19, x20   # 写入x0，源操作数需要读端口
    add x0, x21, x22   # 写入x0，源操作数需要读端口
    add x0, x23, x24   # 写入x0，源操作数需要读端口
    add x0, x25, x26   # 写入x0，源操作数需要读端口
    add x0, x27, x28   # 写入x0，源操作数需要读端口

